/// Helper trait for string case conversion
trait StringCase {
    fn to_snake_case(&self) -> String;
}

impl StringCase for str {
    fn to_snake_case(&self) -> String {
        self.chars()
            .enumerate()
            .map(|(i, c)| {
                if c.is_uppercase() {
                    if i > 0 {
                        format!("_{}", c.to_lowercase().collect::<String>())
                    } else {
                        c.to_lowercase().collect()
                    }
                } else {
                    c.to_string()
                }
            })
            .collect()
    }
}

/// Generate unit test for a Rust function
pub fn generate_unit_test(function: &FunctionInfo, _options: &TestGeneratorOptions) -> String {
    let test_name = format!("test_{}", function.name.to_snake_case());

    format!(
        r#"#[cfg(test)]
mod tests {{
    use super::*;

    #[test]
    fn {test_name}() {{
        // TODO: Implement test
        {setup}
    }}
{additional_tests}
}}"#,
        test_name = test_name,
        setup = generate_test_setup(function),
        additional_tests = generate_additional_tests(function)
    )
}

